	// Editor

	#if UNITY_EDITOR
	[CustomEditor(typeof(AchievementsScreen))]
	class AchievementsScreenEditor : EditorExtensions {
		AchievementsScreen I => target as AchievementsScreen;
		public override void OnInspectorGUI() {
			Begin();

			LabelField("Selected", EditorStyles.boldLabel);
			I.DefaultSelected = ObjectField("Default Selected", I.DefaultSelected);
			Space();

			End();
		}
	}
